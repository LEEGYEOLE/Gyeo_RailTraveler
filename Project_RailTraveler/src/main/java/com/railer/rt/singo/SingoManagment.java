package com.railer.rt.singo;

public class SingoManagment {
	
	private String userName;
	private String targetUserId;
	private int singoCount;
	private int singoNum;
	private int singoListCount;
	private int enabled;
	
	
	
	
	
	

	public int getEnabled() {
		return enabled;
	}
	public void setEnabled(int enabled) {
		this.enabled = enabled;
	}
	public int getSingoNum() {
		return singoNum;
	}
	public void setSingoNum(int singoNum) {
		this.singoNum = singoNum;
	}
	public int getSingoListCount() {
		return singoListCount;
	}
	public void setSingoListCount(int singoListCount) {
		this.singoListCount = singoListCount;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getTargetUserId() {
		return targetUserId;
	}
	public void setTargetUserId(String targetUserId) {
		this.targetUserId = targetUserId;
	}
	public int getSingoCount() {
		return singoCount;
	}
	public void setSingoCount(int singoCount) {
		this.singoCount = singoCount;
	}
	
	
	
}
